# List
print("List")
my_list = [1, 2, 3, 4, 5]
for element in my_list:
    print(element)

# Set
print("Set")
my_set = {1, 2, 3, 4, 5}
for element in my_set:
    print(element)

# Tuple
print("Tuple")
my_tuple = (1, 2, 3, 4, 5)
for element in my_tuple:
    print(element)

# Dictionary
print("Dictionary")
my_dict = {"a": 1, "b": 2, "c": 3}
for key in my_dict:
    print(key, my_dict[key])